    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            
            if (txtNama.getText().equals("") || txtBagian.getText().equals("") || txtUser.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data tidak lengkap");
            } else if (!txtBagian.getText().equals("admin") && !txtBagian.getText().equals("kasir")) {
                JOptionPane.showMessageDialog(null, "Hanya bagian Admin dan Kasir yang dapat mengakses program ini");
            } else if (txtPass.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Masukkan password lama");
            } else {
                if (!txtPassb.getText().equals("") && !txtPassb2.getText().equals("")) {
                    st = con.createStatement();
                    String ck = "SELECT * FROM profil WHERE username = '"+ txtUser.getText() +"' AND password = '"+ txtPass.getText() +"'";
                    rs = st.executeQuery(ck);
                    
                    if (rs.next()) {
                        if (!txtPassb2.getText().equals(txtPassb.getText())) {
                            JOptionPane.showMessageDialog(null, "Masukkan konfirmasi password baru dengan benar !");
                            txtPassb.setText("");
                            txtPassb2.setText("");
                        } else {
                            String in = "UPDATE profil SET nama = '"+ txtNama.getText() +"', bagian = '"+ txtBagian.getText() +"', username = '"+ txtUser.getText() +"', password = '"+ txtPassb2.getText() +"' WHERE id_log = '"+ txtU.getText() +"'";
                            st.executeUpdate(in);

                            st = con.createStatement();
                            String cek1 = "SELECT * FROM user_log ORDER BY id_login DESC LIMIT 1";
                            rs = st.executeQuery(cek1);

                            if(rs.next()) {
                                String idl = rs.getString("id_login");
                                String inn = "UPDATE user_log SET nama = '"+ txtNama.getText() +"', bagian = '"+ txtBagian.getText() +"' WHERE id_login = '"+ idl +"'";
                                st.executeUpdate(inn);
                            }
                            JOptionPane.showMessageDialog(null, "Data user berhasil diubah");
                            loginLevel();
                            txtPassb.setText("");
                            txtPassb2.setText("");
                            txtPass.setText("");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Username atau password salah");
                    }
                    
                } else if (!txtPassb.getText().equals("") && txtPassb2.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Masukkan konfirmasi password baru !");
                } else if (txtPassb.getText().equals("") && txtPassb2.getText().equals("")) {
                    String in2 = "UPDATE profil SET nama = '"+ txtNama.getText() +"', bagian = '"+ txtBagian.getText() +"', username = '"+ txtUser.getText() +"' WHERE id_log = '"+ txtU.getText() +"'";
                    st.executeUpdate(in2);
                    
                    st = con.createStatement();
                    String cek1 = "SELECT * FROM user_log ORDER BY id_login DESC LIMIT 1";
                    rs = st.executeQuery(cek1);

                    if(rs.next()) {
                        String idl2 = rs.getString("id_login");
                        String inn2 = "UPDATE user_log SET nama = '"+ txtNama.getText() +"', bagian = '"+ txtBagian.getText() +"' WHERE id_login = '"+ idl2 +"'";
                        st.executeUpdate(inn2);
                    }
                    JOptionPane.showMessageDialog(null, "Data user berhasil diubah");
                    loginLevel();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

