    private void daftarUser() {
        try {
            String tampil = "SELECT * FROM profil";
            st = con.createStatement();
            rs = st.executeQuery(tampil);
            
            DefaultTableModel mdl = new DefaultTableModel();
            
            mdl.addColumn("ID USER");
            mdl.addColumn("NAMA USER");
            mdl.addColumn("BAGIAN");
            mdl.addColumn("USERNAME");
            mdl.addColumn("PASSWORD");
            
            mdl.getDataVector();
            mdl.fireTableDataChanged();
            mdl.setRowCount(0);
            
            while (rs.next()) {
                Object data[] = {
                    rs.getString("id_log"),
                    rs.getString("nama"),
                    rs.getString("bagian"),
                    rs.getString("username"),
                    rs.getString("password")
            };
                mdl.addRow(data);
                tblUser.setModel(mdl);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
